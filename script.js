const title = document.getElementById('main-title');                // Находим элемент с id "main-title" (это заголовок сайта)

const text = title.textContent;                                     // Сохраняем текст, который написан в заголовке

title.textContent = '';                                             // Очищаем заголовок (удаляем весь текст с экрана)

text.split('').forEach((char, index) => {                           // Разбиваем текст на отдельные буквы и обрабатываем каждую по очереди
  setTimeout(() => {                                                // Задержка перед добавлением каждой буквы (чтобы они появлялись одна за одной)
    const span = document.createElement('span');                    // Создаём новый элемент <span> — обёртку для буквы
    span.textContent = char;                                        // Вставляем в span текущую букву
    span.style.opacity = '0';                                       // Делаем эту букву сначала невидимой
    span.style.transition = 'opacity 0.3s ease';                    // Настраиваем плавное появление (прозрачность будет меняться 0.3 секунды)
    title.appendChild(span);                                        // Добавляем эту букву (в виде span) в заголовок на страницу
    requestAnimationFrame(() => {                                   // Просим браузер отобразить следующий кадр с новой буквой
      span.style.opacity = '1';                                     // В этом кадре делаем букву видимой (она "проявляется")
    });
  }, index * 100);                                                  // Каждая следующая буква появляется через 100мс после предыдущей
});
